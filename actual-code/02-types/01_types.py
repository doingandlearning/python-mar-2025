my_favourite_number = 123    # int -> integer
# snake case      # assignment operator

print(my_favourite_number)
print(type(my_favourite_number))

my_favourite_number = "123"   # str -> string
print(my_favourite_number)
print(type(my_favourite_number))

my_favourite_number = 3.14159  # float -> floating point number/decimal number
print(my_favourite_number)
print(type(my_favourite_number))

my_favourite_number = True  # bool -> boolean - True/False
print(my_favourite_number)
print(type(my_favourite_number))

my_favourite_number = None  # NoneType
print(my_favourite_number)
print(type(my_favourite_number))


